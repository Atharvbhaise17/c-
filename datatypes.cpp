#include<iostream>
using namespace std;

int main(){
 
   int a;
   a=20;
   cout<<"size of int is "<<sizeof(a)<<endl; 

   float b;
   b=2.54;
   cout<<"size of float is "<<sizeof(b)<<endl;


   char d; 
   cout<<"size of char is "<<sizeof(d)<<endl;

   bool k;
   cout<<"size of bool is "<<sizeof(k)<<endl;


    return 0;
}